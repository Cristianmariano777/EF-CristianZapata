package com.idat.MicroservicioHospedaje.dto;

public class TuristaDTO {
	
	private Integer codturi;
    private String nomturi;
    private String celturi;
    private String dirturi;
    
	public Integer getCodturi() {
		return codturi;
	}
	public void setCodturi(Integer codturi) {
		this.codturi = codturi;
	}
	public String getNomturi() {
		return nomturi;
	}
	public void setNomturi(String nomturi) {
		this.nomturi = nomturi;
	}
	public String getCelturi() {
		return celturi;
	}
	public void setCelturi(String celturi) {
		this.celturi = celturi;
	}
	public String getDirturi() {
		return dirturi;
	}
	public void setDirturi(String dirturi) {
		this.dirturi = dirturi;
	}

    
}
